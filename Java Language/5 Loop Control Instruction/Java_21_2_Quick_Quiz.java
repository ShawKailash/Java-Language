// quick quiz
// write a java program to print natural numbers from 100 to 200

public class Java_21_2_Quick_Quiz {
    public static void main(String[] args) {
        System.out.println("print the number 100 to 200");
        int i = 100;
        while(i<=200)
        {
            System.out.println(i);
            i++;
        }
    }
}
