public class Java_10_Data_Type {
    public static void main(String[] args) {
        int x=4;
        int y=8;
        int a=x+y;
        float b=45.34f+x;
        System.out.println(a);
        System.out.println(b);
        int i=45;
        System.out.println(i++);
        System.out.println(i);
        int j=56;
        System.out.println(j);
        System.out.println(++j);
    }
}
