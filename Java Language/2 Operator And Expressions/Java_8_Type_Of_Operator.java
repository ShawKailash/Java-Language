public class Java_8_Type_Of_Operator {
    public static void main(String[] args) {
        int a=10;
        int b=6;
        int sum = a+b;
        System.out.println(sum);
        int c=7;
        c*=5;
        System.out.println(c);
        System.out.println(64<5);
        System.out.println(64>5);
        System.out.println(64>5 && 64<34);
    }
}
